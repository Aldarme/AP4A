#include <iostream>

using namespace std;

int main() {
    cout << "Hello World!" << endl << "Input two integers:" << endl;
    int a, b;
    cin >> a;
    cin >> b;
    cout << a << " + " << b << " = " << a + b << endl;
    cout << a << " - " << b << " = " << a - b << endl;
    cout << a << " * " << b << " = " << a * b << endl;
    return 0;
}
